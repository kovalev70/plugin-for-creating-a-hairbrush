namespace HairbrushPlugin.Model
{
    /// <summary>
    /// Представляет тип параметра для использования в приложении.
    /// </summary>
    public enum ParameterType
    {
        /// <summary>
        /// Длина ручки.
        /// </summary>
        HandleLength,

        /// <summary>
        /// Количество зубьев.
        /// </summary>
        NumberOfTeeth,

        /// <summary>
        /// Длина зубьев.
        /// </summary>
        LengthOfTeeth,

        /// <summary>
        /// Расстояние между зубьями.
        /// </summary>
        DistanceBetweenTeeth,

        /// <summary>
        /// Ширина зубьев.
        /// </summary>
        WidthOfTeeth,

        /// <summary>
        /// Ширина ручки.
        /// </summary>
        HandleWidth
    }
}
